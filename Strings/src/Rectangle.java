public class Rectangle {
    public double length;
    public double width;

    public double area() {
        return length * width;
    }

    public double perimetr(double perimetr) {
        System.out.println(perimetr);
        return perimetr;
    }

    public boolean isSquare(boolean square) {
        System.out.println(square);
        return square;
    }

    public void printDetails(double length, double width) {
        System.out.println(length);
        System.out.println(width);
    }
}
